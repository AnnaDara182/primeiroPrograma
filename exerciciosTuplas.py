print("O primeiro dia da semana é:", dias[0])
print("-----------------------------------------")

#Exibir o último dia da semana.

print("O último dia da semana é:", dias[-1])
print("-----------------------------------------")

#Tamanho da tupla.

print("O tamanho da tupla é:", len(dias))
