#Crie uma tupla com os dias da semana.

dias= ("Segunda-feira", "Terça-feira", "Quarta-feira", "Quinta-feira", "Sexta-feira", "Sábado", "Domingo")
print("-----------------------------------------")
print("Os dias da semana são:", dias)
print("-----------------------------------------")

